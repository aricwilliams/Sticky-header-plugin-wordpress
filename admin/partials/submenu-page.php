 <h1>Part 3: Background rgba Color?</h1>
   <p>
     Definition and Usage<br>
The rgba() function define colors using the Red-green-blue-alpha (RGBA) model.<br>
<br>
RGBA color values are an extension of RGB color values with an alpha channel - which specifies the opacity of the color.<br>
CSS Syntax<br>
rgba(red, green, blue, )<br>
Value	Description<br>
red	Defines the intensity of red as an integer between 0 and 255, or as a percentage value between 0% and 100%<br>
green	Defines the intensity of green as an integer between 0 and 255, or as a percentage value between 0% and 100%<br>
blue	Defines the intensity of blue as an integer between 0 and 255, or as a percentage value between 0% and 100%<br>
     
 </p>
 
   
    <h1>Part 4: Background Color Transparent Level?</h1>
     <p>

CSS Syntax<br>
